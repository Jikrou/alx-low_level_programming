#include "3-calc.h"
/**
 * op_add - addition of to integers
 * @a: integer
 * @b: integer
 * Return: an integer
*/
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - subtraction of to integers
 * @a: integer
 * @b: integer
 * Return: an integer
*/

int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - multiplication of to integers
 * @a: integer
 * @b: integer
 * Return: an integer
*/

int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - division of to integers
 * @a: integer
 * @b: integer
 * Return: an integer
*/

int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - modulo of to integers
 * @a: integer
 * @b: integer
 * Return: an integer
*/

int op_mod(int a, int b)
{
	return (a % b);
}
